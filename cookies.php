      <li><strong>Cookies de funcionalidade:</strong> são cookies que memorizam suas preferências e escolhas (como seu nome de usuário)</li>
      <li><strong>Cookies de publicidade:</strong> são cookies que direcionam anúncios em função dos seus interesses e limitam a quantidade de vezes que o anúncio aparece.</li>
      </ol>
